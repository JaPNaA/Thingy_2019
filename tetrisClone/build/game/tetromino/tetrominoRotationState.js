var RotationState;
(function (RotationState) {
    RotationState[RotationState["up"] = 0] = "up";
    RotationState[RotationState["right"] = 1] = "right";
    RotationState[RotationState["down"] = 2] = "down";
    RotationState[RotationState["left"] = 3] = "left";
})(RotationState || (RotationState = {}));
export default RotationState;
